public class Variables2 {
	public static void main(String[] args) {
		int i;
		double x;

		i = 10;
		x = 10.0;

		System.out.println("Value of i: " + i);
		System.out.println("Value of x: " + x);

		System.out.println("Value of i/3: " + i/3);
		System.out.println("Original value of x/3: " + x/3);
	}
}
